    private void AddEmergency(String name, String hospital, String phone) {
        Connection dbconn = DBConnection.connectDB();
        
        if(dbconn!=null){
            try{
            PreparedStatement st = (PreparedStatement) dbconn.prepareStatement("INSERT INTO emergency_service( emergency_name, emergency_hospital, "
                    + "emergency_phone) "
                    + "VALUES (?,?,?)");
            st.setString(1, name);
            st.setString(2, hospital);
            st.setString(3, phone);

            int res = st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Inserted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
           
        }catch(SQLException ex){
            Logger.getLogger(Donor.class.getName()).log(Level.SEVERE, null,ex);
        }
    }
    }

   


