    private void tableEmergencyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEmergencyMouseClicked
         int row = tableEmergency.getSelectedRow();
       String selection = tableEmergency.getModel().getValueAt(row, 0).toString();
       DefaultTableModel model = (DefaultTableModel)tableEmergency.getModel();
       Connection dbconn = DBConnection.connectDB();
      try{
           PreparedStatement st = (PreparedStatement) dbconn.prepareStatement("Select * from emergency_service where emergency_id= "+selection);
           ResultSet res = st.executeQuery();
          
           if(res.next()){
        tfName.setText(res.getString("emergency_name"));//res.getString("donor_name"));
        tfHospital.setText(res.getString("emergency_hospital"));
        tfPhone.setText(res.getString("emergency_phone"));
           }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_tableEmergencyMouseClicked

